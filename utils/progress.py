    (40,  "в—ү в—ү в—ү в—ү в—Ң в—Ң в—Ң в—Ң в—Ң в—Ң",  "40%"),
    (50,  "в—ү в—ү в—ү в—ү в—ү в—Ң в—Ң в—Ң в—Ң в—Ң",  "50%"),
